    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        try {
            // Verifica se há uma linha selecionada
            int linhaSelecionada = tblDenunciados.getSelectedRow();
            if (linhaSelecionada == -1) {
                JOptionPane.showMessageDialog(this, 
                        "Selecione uma denúncia para remover.", 
                        "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Obtém o CPF/CNPJ da linha selecionada (primeira coluna)
            String cpfCnpj = tblDenunciados.getValueAt(linhaSelecionada, 0).toString();
            
            // Confirma a remoção
            int confirmacao = JOptionPane.showConfirmDialog(this, 
                    "Tem certeza que deseja remover a denúncia do CPF/CNPJ " + cpfCnpj + "?", 
                    "Confirmar Remoção", JOptionPane.YES_NO_OPTION);
            
            if (confirmacao == JOptionPane.YES_OPTION) {
                // Remove a denúncia
                boolean sucesso = integration.removerDenuncia(cpfCnpj, this);
                
                if (sucesso) {
                    // Recarrega a tabela
                    carregarDenuncias();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                    "Erro ao remover denúncia: " + e.getMessage(), 
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

