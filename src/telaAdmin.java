    /**
     * Carrega as denúncias do banco de dados na tabela
     */
    private void carregarDenuncias() {
        try {
            // Obtém os dados das denúncias
            Object[][] dados = integration.carregarDenuncias(this);
            
            if (dados != null) {
                // Cria um modelo de tabela com os dados
                DefaultTableModel modelo = new DefaultTableModel(
                        dados,
                        new String[]{"CPF/CNPJ", "NOME", "MOTIVO"}
                ) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false; // Impede a edição das células
                    }
                };
                
                // Define o modelo na tabela
                tblDenunciados.setModel(modelo);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                    "Erro ao carregar denúncias: " + e.getMessage(), 
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

